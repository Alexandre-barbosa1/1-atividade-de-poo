package br.edu.ifs.academico;

public class Coelho extends Animal {
	private String pelagem;
	private double alturaDoPulo;
	private String corDosOlhos;
	private String tamanhoDaOrelha;

	public String getPelagem() {
		return pelagem;
	}

	public void setPelagem(String pelagem) {
		this.pelagem = pelagem;
	}

	public double getAlturaDoPulo() {
		return alturaDoPulo;
	}

	public void setAlturaDoPulo(double alturaDoPulo) {
		this.alturaDoPulo = alturaDoPulo;
	}

	public String getCorDosOlhos() {
		return corDosOlhos;
	}

	public void setCorDosOlhos(String corDosOlhos) {
		this.corDosOlhos = corDosOlhos;
	}

	public String getTamanhoDaOrelha() {
		return tamanhoDaOrelha;
	}

	public void setTamanhoDaOrelha(String tamanhoDaOrelha) {
		this.tamanhoDaOrelha = tamanhoDaOrelha;
	}

}
